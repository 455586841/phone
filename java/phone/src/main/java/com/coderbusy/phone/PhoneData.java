package com.coderbusy.phone;

public class PhoneData {

	public String getCorp() {
		return corp;
	}
	public void setCorp(String corp) {
		this.corp = corp;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getAreaCode() {
		return areaCode;
	}
	public void setAreaCode(String areaCode) {
		this.areaCode = areaCode;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getTelecomOperator() {
		return telecomOperator;
	}
	public void setTelecomOperator(String telecomOperator) {
		this.telecomOperator = telecomOperator;
	}
	public String getVirtualNetworkOperator() {
		return virtualNetworkOperator;
	}
	public void setVirtualNetworkOperator(String virtualNetworkOperator) {
		this.virtualNetworkOperator = virtualNetworkOperator;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	private String corp;
	private String province;
	private String city;
	private String areaCode;
	private String postCode;
	private String telecomOperator;
	private String virtualNetworkOperator;
	private String card;
}
